    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtID, sb, "Mã sinh viên không được để trống");
        DataValidator.validateEmpty(txtFullname, sb, "Tên sinh viên không được để trống");
        if (sb.length() > 0)    {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Error");
            return;
        }
        try {
            Student stud = new Student();
            stud.setId(txtID.getText());
            stud.setFullname(txtFullname.getText());
            stud.setEmail(txtEmail.getText());
            stud.setPhoneNumber(txtPhone.getText());
            stud.setAddress(txtAddress.getText());
            stud.setSex(rdbMale.isSelected()?1:0);        
            
            StudentDao dao = new StudentDao();
            if (dao.insert(stud)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sinh viên đã được lưu", "Notification");
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Sinh viên không được lưu do lỗi", "Warning");
            }
        
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Error");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtID, sb, "Mã sinh viên không được để trống");
        if (sb.length() > 0)    {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Error");
            return;
        }
        if (MessageDialogHelper.showConfirmDialog(parentForm, 
                "Bạn có muốn xóa sinh viên không ?", "Notification")  ==  JOptionPane.NO_OPTION) {
        return;
        }
        try {              
           StudentDao dao = new StudentDao();
           if (dao.delete(txtID.getText())) {
                MessageDialogHelper.showMessageDialog(parentForm, "Sinh viên đã được xóa khỏi CSDL", "Notification");
            } else {
                MessageDialogHelper.showConfirmDialog(parentForm, "Sinh viên không được xóa do lỗi", "Warning");
            }
        
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Error");
        }  
    }//GEN-LAST:event_btnDeleteActionPerformed

