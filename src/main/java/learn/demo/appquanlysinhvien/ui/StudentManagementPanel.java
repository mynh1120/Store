    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
            try {
            int row = tblStudent.getSelectedRow();
            
            if (row >= 0)   {
                String id = (String) tblStudent.getValueAt(row, 0);
                StudentDao dao = new StudentDao();
                Student stud = dao.findById(id);
                
                if (stud != null)   {
                    txtID.setText(stud.getId());
                    txtFullname.setText(stud.getFullname());
                    txtEmail.setText(stud.getEmail());
                    txtPhone.setText(stud.getPhoneNumber());
                    txtAddress.setText(stud.getAddress());
                    rdbFemale.setSelected(stud.getSex()==1?true:false);
                }
            }
        } catch (Exception e)   {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Error");
        }
    }//GEN-LAST:event_tblStudentMouseClicked


