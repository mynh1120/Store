    private void loadBangDiem() {
        try {
            MarkDao dao = new MarkDao();
            List<Mark>list = dao.findAll();
            tblModel.setRowCount(0);
            for (Mark it : list)    {
                tblModel.addRow(new Object[] {
                    it.getID(), it.getFullname(), it.getEnglish(), it.getInformatics(), it.getPE(),
                    (it.getEnglish() + it.getPE() + it.getInformatics()) / 3
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "");
        }
    }

