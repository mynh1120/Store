    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        StringBuilder sb = new StringBuilder();
        DataValidator.validateEmpty(txtID, sb, "Cần nhập mã sinh viên để tìm kiếm");
        DataValidator.validateEmpty(txtEnglish, sb, "Cần nhập điểm môn Tiếng Anh");
        if (sb.length()>0)  {
            MessageDialogHelper.showErrorDialog(parentForm, sb.toString(), "Error");
            return;
        }
        try {
            Mark mk = new Mark();
            mk.setID(txtID.getText());
            mk.setEnglish(Float.parseFloat(txtEnglish.getText()));
            mk.setInformatics(Float.parseFloat(txtInformatics.getText()));
            mk.setPE(Float.parseFloat(txtPE.getText()));
            
            MarkDao dao = new MarkDao();
            if (dao.findById(txtIDSearch.getText()) != null)    {
                if (MessageDialogHelper.showConfirmDialog(parentForm,
                        "Bạn có muốn cập nhật điểm không?", "Notification")== JOptionPane.NO_OPTION)  {
                    return;
                }
                if(dao.update(mk))  {
                    MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã được cập nhật", "Notification");
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Không thể cập nhật bảng điểm", "Notification");
                }
            } else {
            if (dao.insert(mk)) {
                MessageDialogHelper.showMessageDialog(parentForm, "Bảng điểm đã được lưu", "Notifications");
            }else{
                MessageDialogHelper.showMessageDialog(parentForm, "Không thể lưu bảng điểm", "Notifications");
            }
            }
        }   catch (Exception e) {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Error");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

