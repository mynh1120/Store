    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            MarkDao dao = new MarkDao();
            Mark mk = dao.findById(txtIDSearch.getText());
            
            if (mk!= null)  {
                txtID.setText(mk.getID());
                txtFullname.setText(mk.getFullname());
                txtEnglish.setText(String.format("%.2f", mk.getEnglish()));
                txtPE.setText(String.format("%.2f", mk.getPE()));
                txtInformatics.setText(String.format("%.2f", mk.getInformatics()));
                
                txtIDFocusLost(null);
                txtEnglishFocusLost(null);
           } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Không tìm thấy sinh viên", "Notification");
           }
        } catch (Exception e)   {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getLocalizedMessage(), "Error");
        }     
    }//GEN-LAST:event_btnSearchActionPerformed

