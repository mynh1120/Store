    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if (txtID.getText().equals("")) {
                MessageDialogHelper.showMessageDialog(parentForm, "Mã sinh viên cần phải nhập", "Error");
                return;
            }
            MarkDao dao = new MarkDao();
            Mark mk = dao.findById(txtIDSearch.getText());
            if(mk == null)  {
                MessageDialogHelper.showMessageDialog(parentForm, "Mã sinh viên không tồn tại trong bảng điểm", "Error");
                return;
            }
            if(MessageDialogHelper.showConfirmDialog(parentForm, 
                    "Bạn có muốn xóa điểm của sinh viên không", "Notification") == JOptionPane.NO_OPTION){
                return;
            }
            if(dao.deleteById(txtID.getText()))   {
                MessageDialogHelper.showMessageDialog(parentForm, "Điểm của sinh viên đã được xóa", "Notification");
        }else {
                MessageDialogHelper.showMessageDialog(parentForm, "Điểm của sinh viên không thể xóa", "Notification");
            }               
        } catch (Exception e)   {
            e.printStackTrace();
            MessageDialogHelper.showErrorDialog(parentForm, e.getMessage(), "Error");
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void txtEnglishFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtEnglishFocusLost
        if (txtEnglish.getText().equals("") || txtPE.getText().equals("")  || txtInformatics.getText().equals(""))  {
        return;
            }
           float el = Float.parseFloat(txtEnglish.getText());
           float pe = Float.parseFloat(txtPE.getText());
           float it = Float.parseFloat(txtInformatics.getText());
                
           float avg = (el +pe+it) /3;
                
           String st = String.format("%.2f", avg);
           txtMark.setText(st);
    }//GEN-LAST:event_txtEnglishFocusLost

